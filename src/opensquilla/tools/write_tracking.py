def scratch_only_progress_note() -> str:
    """Return a compact progress nudge after repeated scratch-only writes."""

    ctx = current_tool_context.get()
    if ctx is None:
        return ""
    scratch_writes = len(getattr(ctx, "scratch_file_writes", []) or [])
    if scratch_writes not in _SCRATCH_ONLY_NUDGE_COUNTS and scratch_writes % 10 != 0:
        return ""
    workspace_writes = len(getattr(ctx, "workspace_file_writes", []) or [])
    if workspace_writes:
        return ""
    return (
        " Note: this turn has written "
        f"{scratch_writes} scratch file(s) and no real workspace file changes yet. "
        "Scratch files are temporary; if the task requires changing the project, "
        "use the scratch findings to edit the actual workspace file(s) before final."
    )


def workspace_write_progress_note() -> str:
    ctx = current_tool_context.get()
    if ctx is None:
        return ""
    workspace_writes = len(getattr(ctx, "workspace_file_writes", []) or [])
    if workspace_writes not in _WORKSPACE_WRITE_PROGRESS_COUNTS and workspace_writes % 10 != 0:
        return ""
    return (
        " Note: workspace changes are now present. Before final, inspect git_diff "
        "and run focused verification for the changed behavior."
    )


def workspace_write_note(path: Path) -> str:
    """Return a short hygiene note for risky workspace write targets."""

    relative_path = _workspace_relative_path(path)
    if relative_path is None:
        return ""

    normalized = relative_path.replace("\\", "/").lstrip("./")
    notes: list[str] = []
    if _looks_generated_or_derived(path, normalized):
        notes.append(
            "generated or derived-looking file; confirm it is a source of truth or "
            "regenerate/verify or remove it before final"
        )
    if _matches_any(normalized, _DOCUMENTATION_PATH_PATTERNS):
        notes.append("documentation file; verify the docs build if retaining this change")
    if _looks_test_file(normalized):
        notes.append(
            "test file; only retain this change if the user explicitly requested "
            "test updates, otherwise move the fix to source files or revert it before final"
        )
    if not notes:
        return ""
    return " (note: " + "; ".join(notes) + ")"


def summarize_workspace_write_notes(paths: list[Path]) -> str:
    """Return one compact note covering a set of workspace write targets."""

    saw_generated = False
    saw_docs = False
    saw_tests = False
    for path in paths:
        relative_path = _workspace_relative_path(path)
        if relative_path is None:
            continue
        normalized = relative_path.replace("\\", "/").lstrip("./")
        saw_generated = saw_generated or _looks_generated_or_derived(path, normalized)
        saw_docs = saw_docs or _matches_any(normalized, _DOCUMENTATION_PATH_PATTERNS)
        saw_tests = saw_tests or _looks_test_file(normalized)

    notes: list[str] = []
    if saw_generated:
        notes.append(
            "generated or derived-looking file(s) changed; confirm they are sources "
            "of truth or regenerate/verify or remove them before final"
        )
    if saw_docs:
        notes.append("documentation file(s) changed; verify the docs build if retaining them")
    if saw_tests:
        notes.append(
            "test file(s) changed; only retain them if the user explicitly requested "
            "test updates, otherwise move the fix to source files or revert them before final"
        )
    if not notes:
        return ""
    return " Note: " + "; ".join(notes) + "."


def summarize_patch_hygiene_warning(paths: list[Path]) -> str:
    """Return an explicit final-diff warning for likely risky changed paths."""

    saw_generated = False
    saw_docs = False
    saw_tests = False
    for path in paths:
        relative_path = _workspace_relative_path(path)
        if relative_path is None:
            continue
        normalized = relative_path.replace("\\", "/").lstrip("./")
        saw_generated = saw_generated or _looks_generated_or_derived(path, normalized)
        saw_docs = saw_docs or _matches_any(normalized, _DOCUMENTATION_PATH_PATTERNS)
        saw_tests = saw_tests or _looks_test_file(normalized)

    notes: list[str] = []
    if saw_generated:
        notes.append(
            "generated or derived-looking file(s) are present in the final diff; "
            "unless those are intentional source-of-truth changes, remove them before final"
        )
    if saw_docs:
        notes.append(
            "documentation file(s) are present in the final diff; verify the docs build "
            "from a clean checkout, and remove them if the task does not require docs "
            "changes; timestamp-only workarounds do not travel with the final patch"
        )
    if saw_tests:
        notes.append(
            "test file(s) are present in the final diff; if the user did not explicitly "
            "ask for test updates, revert them and put the functional change in source files"
        )
    if not notes:
        return ""
    return "[patch_hygiene_warning] " + "; ".join(notes) + "."


def _read_state_key(path: Path) -> str:
    return str(path.resolve(strict=False))


def _fingerprint_file(path: Path) -> dict[str, int | str]:
    stat = path.stat()
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return {
        "size": stat.st_size,
        "mtime_ns": stat.st_mtime_ns,
        "sha256": digest.hexdigest(),
    }


def _workspace_relative_path(path: Path) -> str | None:
    ctx = current_tool_context.get()
    if ctx is None or not ctx.workspace_dir:
        return None
    workspace = Path(ctx.workspace_dir).expanduser().resolve()
    try:
        return path.resolve(strict=False).relative_to(workspace).as_posix()
    except ValueError:
        return None


def _path_is_inside_scratch(normalized: str) -> bool:
    ctx = current_tool_context.get()
    if ctx is None or not ctx.workspace_dir or not ctx.scratch_dir:
        return False
    workspace = Path(ctx.workspace_dir).expanduser().resolve(strict=False)
    scratch = Path(ctx.scratch_dir).expanduser().resolve(strict=False)
    try:
        scratch_relative = scratch.relative_to(workspace).as_posix()
    except ValueError:
        return False
    return normalized == scratch_relative or normalized.startswith(f"{scratch_relative}/")


def _matches_any(path: str, patterns: tuple[str, ...]) -> bool:
    return any(fnmatchcase(path, pattern) for pattern in patterns)


def _looks_generated_or_derived(path: Path, normalized: str) -> bool:
    if _matches_any(normalized, _GENERATED_OR_DERIVED_PATH_PATTERNS):
        return True
    return _has_generated_content_marker(path)


def _looks_test_file(normalized: str) -> bool:
    return _matches_any(normalized, _TEST_PATH_PATTERNS) or any(
        pattern.search(normalized) for pattern in _TEST_PATH_REGEX_PATTERNS
    )


def _looks_scratch_artifact_path(normalized: str) -> bool:
    return any(pattern.search(normalized) for pattern in _SCRATCH_ARTIFACT_PATH_REGEX_PATTERNS)


def _has_generated_content_marker(path: Path) -> bool:
    try:
        if not path.is_file():
            return False
        sample = path.read_text(encoding="utf-8", errors="ignore")[:4096].casefold()
    except OSError:
        return False
    return any(marker in sample for marker in _GENERATED_CONTENT_MARKERS)
