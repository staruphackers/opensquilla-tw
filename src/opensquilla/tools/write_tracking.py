from __future__ import annotations

import hashlib
import re
import subprocess
from fnmatch import fnmatchcase
from pathlib import Path
from typing import Any

from opensquilla.tools.types import RetryableToolInputError, current_tool_context

_GENERATED_OR_DERIVED_PATH_PATTERNS = (
    "*.prebuilt",
    "*.generated.*",
    "*.min.*",
    "*.bundle.*",
    "dist/**",
    "build/**",
    "target/**",
    "generated/**",
    "gen/**",
)
_DOCUMENTATION_PATH_PATTERNS = (
    "docs/**",
    "doc/**",
    "documentation/**",
    "manual/**",
    "**/manual/**",
)
_TEST_PATH_PATTERNS = (
    "test/**",
    "tests/**",
    "__tests__/**",
    "**/__tests__/**",
    "*.spec.*",
    "*.test.*",
    "**/*.spec.*",
    "**/*.test.*",
)
_TEST_PATH_REGEX_PATTERNS = (
    re.compile(r"(^|/)test_[^/]+\.[^/]+$", re.I),
    re.compile(r"(^|/)[^/]+_test\.[^/]+$", re.I),
    re.compile(r"(^|/)[^/]*test[^/]*\.(py|js|ts|tsx|rb|php|sh|txt|java|go|zsh)$", re.I),
)
_SCRATCH_ARTIFACT_PATH_REGEX_PATTERNS = (
    re.compile(r"(^|/)(tmp|temp|scratch)(/|$)", re.I),
    re.compile(r"(^|/)\.?(pytest_cache|mypy_cache|ruff_cache|phpunit\.cache)(/|$)", re.I),
    re.compile(
        r"^[^/]*(debug|repro|reproduce|scratch|verify|inspect|investigate)[^/]*"
        r"\.(py|js|ts|rb|php|sh|txt|md|json|yaml|yml|patch|diff|zsh)$",
        re.I,
    ),
    re.compile(r"^[^/]*(debug|repro|reproduce|scratch)[^/]*/", re.I),
    re.compile(r"(^|/)[^/]*\.(patch|diff)$", re.I),
)
_GENERATED_CONTENT_MARKERS = (
    "a bison parser, made by gnu bison",
    "code generated",
    "automatically generated",
    "auto-generated",
    "do not edit",
    "do not modify",
    "generated by",
    "this file was generated",
)
_SCRATCH_ONLY_NUDGE_COUNTS = frozenset({3, 6, 10})
_WORKSPACE_WRITE_PROGRESS_COUNTS = frozenset({1, 3, 6, 10})


def record_workspace_file_write(
    path: Path,
    *,
    operation: str = "write",
    created: bool = False,
) -> None:
    ctx = current_tool_context.get()
    if ctx is None or not ctx.workspace_dir:
        return
    workspace = Path(ctx.workspace_dir).expanduser().resolve()
    try:
        relative_path = path.resolve(strict=False).relative_to(workspace)
    except ValueError:
        return
    ctx.workspace_file_writes.append(
        {
            "path": str(path.resolve(strict=False)),
            "relative_path": relative_path.as_posix(),
            "name": path.name,
            "suffix": path.suffix.casefold(),
            "operation": operation,
            "created": bool(created),
        }
    )


def mutation_ledger_text_hash(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8", errors="replace")).hexdigest()


def snapshot_current_workspace_mutations() -> dict[str, str]:
    ctx = current_tool_context.get()
    if ctx is None or not ctx.workspace_dir:
        return {}
    return snapshot_workspace_mutations(Path(ctx.workspace_dir).expanduser().resolve())


def snapshot_workspace_mutations(workspace: Path) -> dict[str, str]:
    try:
        completed = subprocess.run(
            [
                "git",
                "status",
                "--porcelain=v1",
                "-z",
                "--untracked-files=all",
            ],
            cwd=workspace,
            check=False,
            stdout=subprocess.PIPE,
            stderr=subprocess.DEVNULL,
        )
    except OSError:
        return {}
    if completed.returncode != 0:
        return {}
    return _parse_git_status_z(completed.stdout.decode("utf-8", errors="replace"))


def record_observed_workspace_mutations(
    *,
    tool_name: str,
    before: dict[str, str],
    metadata: dict[str, Any] | None = None,
) -> None:
    ctx = current_tool_context.get()
    if ctx is None or not ctx.workspace_dir or ctx.on_runtime_event is None:
        return
    after = snapshot_current_workspace_mutations()
    paths = diff_workspace_mutations(before, after)
    if not paths:
        return
    ctx.workspace_mutation_records.append(
        {
            "tool": tool_name,
            "tool_name": tool_name,
            "operation": "observe_only",
            "paths": paths,
            "path_count": len(paths),
            **(metadata or {}),
        }
    )
    event: dict[str, Any] = {
        "feature": "mutation_ledger",
        "name": "workspace_mutation_observed",
        "tool": tool_name,
        "tool_name": tool_name,
        "operation": "observe_only",
        "paths": paths,
        "path_count": len(paths),
        "agent_id": getattr(ctx, "agent_id", None),
        "session_key": getattr(ctx, "session_key", None),
    }
    if metadata:
        event.update(metadata)
    try:
        ctx.on_runtime_event(event)
    except Exception:
        return


def diff_workspace_mutations(
    before: dict[str, str],
    after: dict[str, str],
) -> list[dict[str, str]]:
    paths: list[dict[str, str]] = []
    for relative_path in sorted(set(before) | set(after)):
        before_status = before.get(relative_path)
        after_status = after.get(relative_path)
        if before_status == after_status:
            continue
        paths.append(
            {
                "relative_path": relative_path,
                "status": after_status or "deleted",
                "classification": classify_workspace_path(relative_path),
            }
        )
    return paths


def classify_workspace_path(relative_path: str) -> str:
    normalized = relative_path.replace("\\", "/").lstrip("./")
    if _path_is_inside_scratch(normalized):
        return "scratch"
    path = Path(normalized)
    if _looks_test_file(normalized):
        return "test-like"
    if _looks_scratch_artifact_path(normalized):
        return "scratch"
    if _matches_any(normalized, _DOCUMENTATION_PATH_PATTERNS):
        return "docs"
    if _looks_generated_or_derived(path, normalized):
        return "generated"
    if normalized:
        return "source"
    return "unknown"


def _parse_git_status_z(output: str) -> dict[str, str]:
    records: dict[str, str] = {}
    entries = output.split("\0")
    index = 0
    while index < len(entries):
        entry = entries[index]
        index += 1
        if not entry:
            continue
        status = entry[:2]
        relative_path = entry[3:] if len(entry) > 3 else ""
        if not relative_path:
            continue
        if status[:1] in {"R", "C"} and index < len(entries):
            relative_path = entries[index] or relative_path
            index += 1
        records[relative_path.replace("\\", "/")] = status
    return records


def record_workspace_file_read(
    path: Path,
    *,
    operation: str = "read",
    offset: int | None = None,
    limit: int | None = None,
    complete: bool | None = None,
) -> None:
    ctx = current_tool_context.get()
    if ctx is None or not ctx.workspace_dir:
        return
    workspace = Path(ctx.workspace_dir).expanduser().resolve()
    try:
        relative_path = path.resolve(strict=False).relative_to(workspace)
    except ValueError:
        return
    resolved = path.resolve(strict=False)
    read_record = {
        "path": str(resolved),
        "relative_path": relative_path.as_posix(),
        "name": path.name,
        "suffix": path.suffix.casefold(),
        "operation": operation,
        "offset": offset,
        "limit": limit,
    }
    if complete is not None:
        read_record["complete"] = bool(complete)
    ctx.workspace_file_reads.append(read_record)
    if complete:
        try:
            ctx.workspace_file_read_state[_read_state_key(resolved)] = {
                "path": str(resolved),
                "relative_path": relative_path.as_posix(),
                "operation": operation,
                **_fingerprint_file(resolved),
            }
        except OSError:
            return


