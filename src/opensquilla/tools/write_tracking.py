def require_fresh_workspace_file_read(
    path: Path,
    *,
    tool_name: str,
    original_path: str,
) -> None:
    ctx = current_tool_context.get()
    if ctx is None or not ctx.workspace_dir:
        return
    if not ctx.file_edit_requires_fresh_read:
        return
    workspace = Path(ctx.workspace_dir).expanduser().resolve()
    resolved = path.resolve(strict=False)
    try:
        relative_path = resolved.relative_to(workspace)
    except ValueError:
        return
    if ctx.scratch_dir:
        scratch = Path(ctx.scratch_dir).expanduser().resolve(strict=False)
        try:
            resolved.relative_to(scratch)
            return
        except ValueError:
            pass
    if not resolved.exists():
        return

    key = _read_state_key(resolved)
    state = ctx.workspace_file_read_state.get(key)
    if state is None:
        action = "edit" if tool_name == "edit_file" else "write"
        _record_fresh_read_guard_event(
            ctx=ctx,
            tool_name=tool_name,
            original_path=original_path,
            resolved=resolved,
            relative_path=relative_path.as_posix(),
            reason="missing_fresh_read",
        )
        raise RetryableToolInputError(
            f"{tool_name} must read existing workspace file before modifying it: "
            f"{original_path}. Use read_file without offset or limit, then retry the {action}."
        )

    try:
        current = _fingerprint_file(resolved)
    except OSError as exc:
        _record_fresh_read_guard_event(
            ctx=ctx,
            tool_name=tool_name,
            original_path=original_path,
            resolved=resolved,
            relative_path=relative_path.as_posix(),
            reason="fingerprint_unavailable",
        )
        raise RetryableToolInputError(
            f"{tool_name} could not verify the current file before modifying it: "
            f"{original_path}. Use read_file without offset or limit, then retry."
        ) from exc
    fingerprint_keys = ("size", "sha256")
    if any(state.get(key) != current.get(key) for key in fingerprint_keys):
        _record_fresh_read_guard_event(
            ctx=ctx,
            tool_name=tool_name,
            original_path=original_path,
            resolved=resolved,
            relative_path=relative_path.as_posix(),
            reason="stale_fresh_read",
        )
        raise RetryableToolInputError(
            f"{original_path} changed since it was read. Use read_file without "
            f"offset or limit to refresh context, then retry {tool_name}."
        )


def _record_fresh_read_guard_event(
    *,
    ctx,
    tool_name: str,
    original_path: str,
    resolved: Path,
    relative_path: str,
    reason: str,
) -> None:
    callback = getattr(ctx, "on_runtime_event", None)
    if callback is None:
        return
    event = {
        "feature": "fresh_read_guard",
        "name": "fresh_read_guard.blocked",
        "tool": tool_name,
        "tool_name": tool_name,
        "path": original_path,
        "resolved_path": str(resolved),
        "relative_path": relative_path,
        "reason": reason,
        "outcome": "blocked",
        "agent_id": getattr(ctx, "agent_id", None),
        "session_key": getattr(ctx, "session_key", None),
    }
    try:
        callback(event)
    except Exception:
        return


